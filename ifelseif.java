package DSA;

public class ifelseif 
{
	public static void main(String[] args)
	{
		int n1=10;
		int n2=20;
		if(n1==n2)
		{
			System.out.println(n1+"equal to "+n2);
		}
		else
		{
			if(n1>n2)
			{
				System.out.println(n1+"to greater then"+n2);
			}
			else
			{
				System.out.println(n1+"is smaller then"+n2);
			}
		}
	}

}
